int main(){
    int n;
    cout<< "Enter n:";
    cin>> n;

    for(int i=1;i<=n; i++){
        for(int j=n-i; j>=1; j--){
            cout << " ";
        }
        for(int j=1; j<=i; j++){
            cout<<"* ";
        }
        cout << endl;
    }
    for(int i=0; i<n; i++){
        for(int k =1; k<=i; k++){
            cout<< " ";
        }
        for(int j = n-i; j>=1; j--){
            cout<< "* ";
        }
        cout<< endl;
    }
}